mood = input("What is your mood today?")

if mood == "happy" :
    print("It is great to see you happy!")

elif mood == "nervous" :
    print("Take a deep breath 3 times.")

elif mood == "sad" :
    print("Take a candy!")

elif mood == "excited" :
    print("Go and run!")

elif mood == "relaxed" :
    print("Fun day, isnt it?")

else :
    print("I dont recognize this mood")